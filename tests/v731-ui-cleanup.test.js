assert.match(version,/ui-cleanup-v732\.css\?v=7\.3\.2/,'Cleanup-CSS wird nicht geladen');
assert.match(manifest,/version:'7\.3\.2'/,'Cacheversion ist nicht 7.3.2');
assert.match(manifest,/ui-cleanup-v732\.css\?v=7\.3\.2/,'Cleanup-CSS fehlt im Cache');
assert.match(sw,/runtime-manifest\.js\?v=7\.3\.2/,'Service Worker lädt falsches Manifest');
assert.match(update,/sw\.js\?v=7\.3\.2-force/,'Update-Seite installiert nicht 7.3.2');

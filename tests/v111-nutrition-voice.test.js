const manifest=fs.readFileSync(path.join(project,'runtime-manifest.js'),'utf8');
const sw=fs.readFileSync(path.join(project,'sw.js'),'utf8');
const update=fs.readFileSync(path.join(project,'update.html'),'utf8');
assert.match(js,/SpeechRecognition\|\|window\.webkitSpeechRecognition/,'SpeechRecognition-Fallback fehlt');
assert.match(js,/stopImmediatePropagation/,'Alte Mikrofonlogik wird nicht zuverlässig abgefangen');
assert.match(js,/aria-pressed/,'Aufnahmestatus ist nicht zugänglich');
assert.match(js,/12000/,'Sicherheits-Timeout für die Aufnahme fehlt');

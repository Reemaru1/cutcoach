'use strict';
const assert=require('node:assert/strict');
const fs=require('node:fs');
const path=require('node:path');
const project=path.resolve(__dirname,'..');
const js=fs.readFileSync(path.join(project,'nutrition-voice-111.js'),'utf8');
const css=fs.readFileSync(path.join(project,'nutrition-voice-111.css'),'utf8');

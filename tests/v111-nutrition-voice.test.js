assert.match(js,/pagehide/,'Aufnahme wird beim Verlassen nicht beendet');
assert.match(js,/insertFromDictation/,'Erkannter Text wird nicht als Diktat an die Suche übergeben');
assert.match(js,/not-allowed/,'Fehlerbehandlung für Mikrofonrechte fehlt');
assert.match(css,/nutritionVoice\.listening/,'Sichtbarer Aufnahmezustand fehlt');
